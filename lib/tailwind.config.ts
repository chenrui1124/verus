const vColors = {
  bsc: ['255, 255, 255', '28, 27, 30'],
  'on-bsc': ['25, 25, 30', '240, 240, 245'],

  pri: ['0, 94, 177', '166, 200, 255'],
  'on-pri': ['255, 255, 255', '0, 48, 96'],

  nrm: ['195, 230, 255', '50, 70, 110'],
  'on-nrm': ['0, 20, 40', '213, 227, 255'],

  'nrm-var': ['236, 247, 255', '35, 40, 55'],

  err: ['152, 64, 97', '255, 177, 200'],
  'on-err': ['255, 255, 255', '94, 17, 51'],

  'err-ctr': ['255, 217, 226', '123, 41, 73'],
  'on-err-ctr': ['62, 0, 29', '255, 217, 226'],

  'err-ctr-var': ['255, 243, 246', '59, 31, 43'],

  otl: ['148, 143, 153', '122, 117, 127'],

  dis: ['200, 200, 200', '80, 80, 80']
}

type VColors = keyof typeof vColors

const toVar = (v: VColors) => `--color-${v}`

const theme = {
  opacity: {
    0: '0',
    8: '0.08',
    12: '0.12',
    16: '0.16',
    32: '0.32',
    48: '0.48',
    72: '0.72',
    100: '1'
  },
  borderRadius: {
    inherit: 'inherit',
    none: '0',
    v1: '6.4px',
    v2: '12.8px',
    v3: '19.6px',
    full: '9999px'
  },
  borderWidth: {
    0: '0',
    DEFAULT: '1.2px'
  },
  colors: ({ colors }) => ({
    black: colors.black,
    white: colors.white,
    inherit: colors.inherit,
    current: colors.current,
    transparent: colors.transparent,
    ...themeReducer(
      Object.keys(vColors),
      name => `rgba(var(${toVar(<VColors>name)}), <alpha-value>)`
    )
  }),
  extend: {
    transitionTimingFunction: {
      braking: 'cubic-bezier(0.36, 0.72, 0, 1)'
    },
    transitionDuration: {
      inherit: 'inherit'
    },
    lineHeight: ({ theme }) => ({
      12: theme('height.12')
    })
  }
} satisfies Config['theme']

const base = {
  '::before, ::after': {
    '--tw-content': `''`
  },
  ':root': Object.keys(vColors).reduce<Record<string, string>>((vars, v) => {
    vars[toVar(<VColors>v)] = vColors[<VColors>v][0]
    return vars
  }, {}),
  ':root:is(.dark)': Object.keys(vColors).reduce<Record<string, string>>((vars, v) => {
    vars[toVar(<VColors>v)] = vColors[<VColors>v][1]
    return vars
  }, {})
} satisfies CSSInJS

const utilities = (theme => ({
  '.v-outline-none': {
    outline: '0px solid transparent',
    outlineOffset: '2px'
  },
  '.v-outline': {
    outline: `2px solid rgba(var(${toVar('pri')}), ${theme('opacity.32')})`,
    outlineOffset: '2px'
  },
  '.v-outline-danger': {
    outline: `2px solid rgba(var(${toVar('err')}), ${theme('opacity.32')})`,
    outlineOffset: '2px'
  },
  '.v-disabled': {
    opacity: theme('opacity.48'),
    pointerEvents: 'none'
  }
})) satisfies (theme: (path: string) => string) => CSSInJS

const components = {
  '.v-shade': {
    position: 'absolute',
    inset: '0',
    pointerEvents: 'none',
    borderRadius: 'inherit'
  }
} satisfies CSSInJS

const variants = [
  ['hover&focus', ['&:hover', '&:focus']],
  ['before&after', ['&:before', '&:after']]
] satisfies [string, string[]][]

